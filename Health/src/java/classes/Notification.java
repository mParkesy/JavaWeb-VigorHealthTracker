package classes;

/**
 *
 * @author owner
 */
public class Notification {
    private int id;
    private String desc;

    Notification(int id,String desc){
        this.id = id;
        this.desc = desc;
    }
    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the desc
     */
    public String getDesc() {
        return desc;
    }

    /**
     * @param desc the desc to set
     */
    public void setDesc(String desc) {
        this.desc = desc;
    }
}
